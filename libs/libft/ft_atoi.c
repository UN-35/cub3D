#include "libft.h"

int	ft_atoi(char *str)
{
	int	a;
	int	r;
	int	k;

	k = 0;
	a = 1;
	r = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-')
		a *= -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str == '0')
		str++;
	while (*str >= 48 && *str <= 57)
	{
		r = r * 10 + *str++ - 48;
		k++;
	}
	return (a * r);
}
